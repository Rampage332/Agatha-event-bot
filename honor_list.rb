    def Get_Research_Ref_Code()
        
        honorcode = ''
        
        if @honor[1].downcase == 'outpost'
            honorcode = honorcode + 'op'
            
        elsif @honor[1].downcase == 'troop'
            case @honor[2].downcase
                when 'hp' then honorcode = honorcode + 'trhp'
                when 'def' then honorcode = honorcode + 'trdef'
                when 'atk' then honorcode = honorcode + 'tratk'
