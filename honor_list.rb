            @level = @honor[3]
        
        
        elsif @honor[1].downcase == 'warrior'
        
            case @honor[2].downcase
                when 'atk' then honorcode = honorcode + 'watk'
                when 'hp' then honorcode = honorcode + 'whp'
                when 'def' then honorcode = honorcode + 'wdef'
            end
            
            case @honor[3].downcase
                when 'i' then @tier = 'I'
                when 'ii' then @tier = 'II'
            end
            
            @level = @honor[4]
            
        
            
        elsif @honor[1].downcase == 'shaman'
            case @honor[2].downcase
                when 'atk' then honorcode = honorcode + 'shatk'
                when 'hp' then honorcode = honorcode + 'shhp'
                when 'def' then honorcode = honorcode + 'shdef'
            end
                
            case @honor[3].downcase
                when 'i' then @tier = 'I'
                when 'ii' then @tier = 'II'
            end
            
            @level = @honor[4]
        
                
        elsif @honor[1].downcase == 'beastmaster'
            case @honor[2].downcase
                when 'atk' then honorcode = honorcode + 'bmatk'
                when 'hp' then honorcode = honorcode + 'bmhp'
                when 'def' then honorcode = honorcode + 'bmdef'
            end
                    
            case @honor[3].downcase
                when 'i' then @tier = 'I'
                when 'ii' then @tier = 'II'
            end
            
            @level = @honor[4]
            
            
        end

        honorcode
        
    end
    
